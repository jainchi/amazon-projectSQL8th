	s.shippingproviders,
	COUNT(o.order_id) as order_handled,
	SUM(price_per_unit * quantity) as total_sale,
	COALESCE(AVG(s.return_date - s.shipping_date), 0) as average_days
FROM orders as o
JOIN 
order_items as oi
ON oi.order_id = o.order_id
JOIN 
shipping as s
ON 
s.order_id = o.order_id
