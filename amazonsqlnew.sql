JOIN order_items  oi
ON oi.order_id=o.order_id
GROUP BY 1,2
),
tableb AS (
SELECT montha ,years ,current_monthsales, LAG(current_monthsales,1) OVER( ORDER BY montha) AS premonthsales FROM tablea
WHERE years = 2024)

SELECT montha ,years ,current_monthsales,premonthsales,100*(current_monthsales - premonthsales)/premonthsales FROM tableb


